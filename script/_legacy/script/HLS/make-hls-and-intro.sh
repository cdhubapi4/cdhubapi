for file in "${list[@]}"
do
  # 확장자를 제외한 파일명 추출
  base_name=$(basename "$file" .mp4)

  # 해당 파일명으로 디렉토리 생성
  mkdir -p "$base_name"

  # 인트로 이미지 생성
  ffmpeg -i "$file" -ss 00:00:00 -frames:v 1 "$base_name/intro.png"

  # HLS 변환 (90도 회전 포함)
  ffmpeg -i "$file" -profile:v baseline -level 3.0 -start_number 0 -hls_time 5 -hls_list_size 0 -f hls "$base_name/v.m3u8"
done
